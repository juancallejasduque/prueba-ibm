CREATE TABLE CLIENT(
ID BIGINT  PRIMARY KEY AUTO_INCREMENT,
FULL_NAME VARCHAR(60),
CITY VARCHAR(40),
ADDRESS VARCHAR(120),
PHONE_NUMBER VARCHAR(30)
);

CREATE TABLE CREDIT_CARD(
ID BIGINT  PRIMARY KEY AUTO_INCREMENT,
CLIENT_ID BIGINT, 
CARD_NUMBER VARCHAR(60),
foreign key (CLIENT_ID) references CLIENT(ID)
);